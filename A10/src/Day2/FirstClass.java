package Day2;

public class FirstClass {

	public static void main(String[] args) {
		
		//Print
		int a;								//Integer
		a = 5;
		int b = 10;
		double c = 10.5;					//Float
		char d = 'a';						//Character
		boolean e = true;					//Boolean
		boolean f = false;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println();
		System.out.println("Hi 1");			//This is a print statement to print in new line 
		System.out.println("Hi 2");
		System.out.println("Hi 3");
		System.out.print("Hi Hello 1");		//This is a print statement to print in a same line
		System.out.print("Hi Hello 2");
		System.out.print("Hi Hello 3");
	}

}
